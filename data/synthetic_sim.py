    axes.set_ylim([-2., 2.])

    for i in range(loc.shape[-1]):
        if i < num_visible:
            color = color_visible
            alpha = 1.0
            label_hist = 'Visible history' if i == 0 else None
        else:
            color = color_hidden
            alpha = 0.2
            label_hist = 'Hidden history' if i == num_visible else None

        # 历史轨迹
        plt.plot(
            loc[:T_split, 0, i],
            loc[:T_split, 1, i],
            color=color,
            alpha=alpha,
            linestyle='-',
            label=label_hist
        )
        # 历史起点
        plt.plot(
            loc[0, 0, i], loc[0, 1, i],
            marker='o',
            color=color,
            alpha=alpha
        )

